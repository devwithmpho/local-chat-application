    // updating the JTable element with all the details inside the database
    public void updateTable() {
        UsersController uc = new UsersController();
        List<User> users = uc.getAllUsers();
        
        String[] columns = {"ID", "FirstName", "Surname", "EmailAddress", "HashedPassword", "Type"};
        
        // Establishing the general structure of the table
        DefaultTableModel model = new DefaultTableModel(columns, 0);
        usersTbl.setModel(model);
        
        // loops through all the users in the arraylist of users and adds them as rows in the table
        users.forEach(user -> {
            model.addRow(new Object[]{
                user.getUserId(),
                user.getName(),
                user.getSurname(),
                user.getEmailAddress(),
                user.getHashedPassword(),
                user.getType()
            });
        });
    }

