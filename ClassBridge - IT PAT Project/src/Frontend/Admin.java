    // Method to delete users from table and update the tale
    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        // TODO add your handling code here:
        int id = 0;
        
        // Getting the ID from the selected row or an input dialog
        if (usersTbl.getSelectedRow() != -1) {
            id = (int) usersTbl.getValueAt(usersTbl.getSelectedRow(), 0);
        } else {
            // if the user hasnt selected any row, the usersTbl.getSelectedRow() method will return -1
            id = Integer.parseInt(JOptionPane.showInputDialog("Enter Row ID:"));
        }
        
        if (id > 0) {
            // This is a confirmation panel to ensure that the user actually want to delete the user
            int result = JOptionPane.showConfirmDialog(
                this,
                "Are you sure you want to delete user with ID: " + id,
                "Confirm Delete",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.WARNING_MESSAGE
            );

            if (result == JOptionPane.YES_OPTION) {
                UsersController uc = new UsersController();
                uc.deleteUser(id);

                updateTable();
            } else {
                System.out.println("Deletion cancelled");
            }
        }
    }//GEN-LAST:event_deleteBtnActionPerformed

    private void createNewBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createNewBtnActionPerformed
        // TODO add your handling code here:
        String firstname = JOptionPane.showInputDialog(this, "Enter user First Name:");
        if (firstname == null) { return; }
        
        String surname = JOptionPane.showInputDialog(this, "Enter user Surname:");
        if (surname == null) { return; }
        
        String emailAddress = JOptionPane.showInputDialog(this, "Enter user Email Address:");
        if (emailAddress == null) { return; }
        
        String password = JOptionPane.showInputDialog(this, "Enter user Password:");
        if (password == null) { return; }
        
        String type = JOptionPane.showInputDialog(this, "Enter user type:\n [Student | Parent | Teacher]");
        // validating the type input to ensure that the user enters the correct type
        if (type == null) { return; }
        else if (!type.contentEquals("Student") || !type.contentEquals("Parent") || !type.contentEquals("Teacher")) {
            System.out.println("Type invalid!");
            return;
        }
        
        UsersController uc = new UsersController();
        uc.createUser(firstname, surname, emailAddress.toLowerCase(), password, type);
        
        // updating the table UI to show the newly created user as this isnt done automatically
        updateTable();
    }//GEN-LAST:event_createNewBtnActionPerformed

